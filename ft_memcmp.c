#include "libft.h"

int	ft_memcmp(const void *dst, const void *src, size_t n)
{
	const unsigned char	*p1;
	const unsigned char	*p2;

	p1 = dst;
	p2 = src;
	while (n--)
	{
		if (*p1 != *p2)
		{
			return (*p1 - *p2);
		}
		p1++;
		p2++;
	}
	return (0);
}
